    public void LoadProductNo(){
        try {
            pst = con.prepareStatement("SELECT id FROM user_db");
            rs = pst.executeQuery();
            txtID.removeAllItems();
            while(rs.next()){
                txtID.addItem(rs.getString(1));
            }
        } catch (SQLException ex) {
            Logger.getLogger(admin_crud_2.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private void Fetch(){
        try {
            int q;
            pst = con.prepareStatement("SELECT * FROM user_db");
            rs = pst.executeQuery();
            ResultSetMetaData rss = rs.getMetaData();
            q = rss.getColumnCount();
            
            DefaultTableModel df = (DefaultTableModel)jTable1.getModel();
            df.setRowCount(0);
            while(rs.next()){
                Vector v2 = new Vector();
                for(int a=1; a<=q; a++){
                    v2.add(rs.getString("id"));
                    v2.add(rs.getString("student_id"));
                    v2.add(rs.getString("first_name"));
                    v2.add(rs.getString("last_name"));
                    v2.add(rs.getString("nu_email"));
                }
                df.addRow(v2);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(admin_crud_2.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    
