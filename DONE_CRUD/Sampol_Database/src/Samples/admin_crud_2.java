    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        try {
            String pid = txtID.getSelectedItem().toString();
            
            pst = con.prepareStatement("SELECT * FROM user_db WHERE id=?");
            pst.setString(1, pid);
            rs=pst.executeQuery();
            
            if (rs.next()==true) {
                firstName.setText(rs.getString(2));
                lastName.setText(rs.getString(3));
                studentID.setText(rs.getString(4));
                nuEMAIL.setText(rs.getString(5));
            } else {
                 JOptionPane.showMessageDialog(this, "No Record Found!");
            }
        } catch (SQLException ex) {
            Logger.getLogger(admin_crud_2.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_searchButtonActionPerformed

    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        try {
            String student_id = studentID.getText();
            String first_name = firstName.getText();
            String last_name = lastName.getText();
            String nu_email = nuEMAIL.getText();
            String sid = txtID.getSelectedItem().toString();

            pst = con.prepareStatement("UPDATE user_db SET first_name=?, last_name=?, student_ud=?, nu_email=? WHERE id=?");

            pst.setString(1, student_id);
            pst.setString(2, first_name);
            pst.setString(3, last_name);
            pst.setString(4, sid);
            pst.setString(5, nu_email);

            int k = pst.executeUpdate();
            if (k==1) {
                JOptionPane.showMessageDialog(this, "Record has been successfully updated!");
                firstName.setText("");
                lastName.setText("");
                studentID.setText("");
                nuEMAIL.setText("");
                firstName.requestFocus();
                Fetch();
                LoadProductNo();
            }

        } catch (SQLException ex) {
            Logger.getLogger(admin_crud_2.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_updateButtonActionPerformed

