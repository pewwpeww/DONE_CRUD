    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
       
        try {
            
            String student_id = studentID.getText();
            String first_name = firstName.getText();
            String last_name = lastName.getText();
            String nu_email = nuEMAIL.getText();
            
            pst = con.prepareStatement("INSERT INTO user_db (student_id,first_name,last_name,nu_email)VALUES (?,?,?,?)");
            pst.setString(1, student_id);
            pst.setString(2, first_name);
            pst.setString(3, last_name);
            pst.setString(4, nu_email);
            
            int k = pst.executeUpdate();
            
            if (k==1) {
                JOptionPane.showMessageDialog(this, "Sucessfully Added!");
                firstName.setText("");
                lastName.setText("");
                studentID.setText("");
                nuEMAIL.setText("");
                Fetch();
                LoadProductNo();
                
            }else{
                JOptionPane.showMessageDialog(this, "Record failed!");
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(admin_crud_2.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_submitButtonActionPerformed

